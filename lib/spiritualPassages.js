export function getSpiritualPassage(day) {
  if (day >= 1 && day <= 28) {
    return spiritualPassages[day - 1];
  }
  // For days beyond 28, cycle through passages
  const index = ((day - 1) % 28);
  return spiritualPassages[index];
}

export function getTodaySpiritualPassage() {
  const today = new Date();
  const dayOfMonth = today.getDate();
  const monthIndex = today.getMonth();
  // February 2026 = primary month
  if (monthIndex === 1 && dayOfMonth >= 1 && dayOfMonth <= 28) {
    return spiritualPassages[dayOfMonth - 1];
  }
  // For other months, cycle based on day
  return getSpiritualPassage(dayOfMonth);
}

export function getPassageByDate(dateStr) {
  const date = new Date(dateStr + 'T12:00:00');
  const dayOfMonth = date.getDate();
  return getSpiritualPassage(dayOfMonth);
}
